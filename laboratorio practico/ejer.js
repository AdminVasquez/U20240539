function numero(numer) {
    return numer % 2 === 0;
}

// Ejemplos de uso:
console.log ("El primer numero es: 4")
console.log("El segundo numero es: 7 ")
console.log("Se determinara con TRUE si el numero es par, si es impar se determinara con FALSE")

console.log( "El numero 4 es ", numero(4)); // true
console.log( "El numero 7 es ", numero(7)); // false
